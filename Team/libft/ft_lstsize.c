#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		count;
	t_list	*temp;

	count = 0;
	temp = lst->next;
	while (temp != lst)
	{
		count++;
		temp = temp->next;
	}
	return (count);
}
